// > = maior
// < = menos
// >= maior ou igual
//<= menor ou igual
// == igual, operador de igualdade
// != diferente

let a = 1 > 2;
console.log(a);

let b = 1 < 2;
console.log(b);

let c = 10 >= 2;
console.log(c);

let d = 10 <= 2;
console.log(d);

let e = 10 == "10";
console.log(e);

let f = 10 != "10";
console.log(f);

let g = 10 === "10";
console.log(g);

var x = 5;
var y = "5";
var z = x != y;
console.log(z)

var identidade = x !== y;
console.log(identidade);