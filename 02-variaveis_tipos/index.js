// tipos de variaveiss

// tipo number / infinity ou NaN

var numero = 10;
console.log(numero);

// tipo string/texto

var nome = "Gabriel";
console.log(typeof nome);

// tipo boolean/true ou false

var Verdadeiro = true;
console.log(typeof Verdadeiro);

var Falso = false;
console.log(typeof Falso);

// tipo do array

var array = [];
console.log(typeof array);

//tipo da função

var funcao = function(){};
console.log(typeof funcao);

// tipo undefined

var indefinido;
console.log(indefinido);

// tipo null/nada

var preco = null;
console.log(preco);
