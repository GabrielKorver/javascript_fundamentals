function cliquei(){
    let t = document.querySelector('#container')
    t.innerHTML = "Cliquei!";
    t.style.backgroundColor = "orange";
    t.style.color = "white";
}

function entrar(){
    let t = document.querySelector('#container')
    t.innerHTML = "Entrei";
    t.style.backgroundColor = "green";
    t.style.color = "white";
}

function sair(){
    let t = document.querySelector('#container')
    t.innerHTML = "Sai!"
    t.style.backgroundColor = "Yellow";
    t.style.color = "black"
}